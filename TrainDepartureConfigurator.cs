        static private Train CreateTrain(int numberPassengers)
        {
            return new Train(numberPassengers);
        }

        private void ShowMenu()
        {
            string changeInMenu;
            bool isWorking = true;

            ShowTextOfMenu(0);
            Console.ReadLine();
            Console.Clear();

            while (isWorking)
            {
                if (_formedTrainsDeparture.Count > 0)
                {
                    ShowInformationDepartureLastTrain();
                }

                Console.SetCursorPosition(_cursorPositionX, _cursorPositionY);
                ShowTextOfMenu(1);
                changeInMenu = Console.ReadLine();

                switch (changeInMenu)
                {
                    case "1":
                        CreatDeparture();
                        break;
                    case "2":
                        ShowInformationDepartureAllTrains();
                        break;
                    case "3":
                        isWorking = false;
                        break;
                }
            }
        }

        private void CreatDeparture()
        {                      
            string cityOfArrival = null;
            string cityOfDeparture = null;
            string userInput;
            int numberLineMenu = 3;
            int numberPassengers;
            int numberTicket;
            bool isWork = true;
            bool isRouteSelect = false;

            Console.Clear();

            while (isWork == true)
            {
                ShowTextOfMenu(numberLineMenu);
                ShowTextOfMenu(5);
                userInput = Console.ReadLine();

                if (FindCity(userInput) == false)
                {
                    ShowTextOfMenu(8);
                    isWork = false;
                    isRouteSelect = false;
                    Console.ReadLine();
                }
                else
                {
                    ShowTextOfMenu(6);
                    ShowSelectCities();
                    ShowTextOfMenu(7);
                    userInput = Console.ReadLine();

                    if (GetNumberCity(userInput, out int numberUserInput) == false)
                    {
                        ShowTextOfMenu(8);
                        isWork = false;
                        isRouteSelect = false;
                        Console.ReadLine();
                    }
                    else
                    {
                        if (numberLineMenu == 4)
                        {
                            cityOfArrival = _findNames[numberUserInput];

                            if (cityOfDeparture == cityOfArrival)
                            {
                                Console.Clear();
                                ShowTextOfMenu(19);
                                isWork = false;
                                isRouteSelect = false;
                                Console.ReadLine();
                            }
                            else
                            {
                                ShowTextOfMenu(9);
                                isRouteSelect = true;
                                isWork = false;
                                Console.ReadLine();
                                Console.Clear();
                            }
                        }
                        else
                        {
                            cityOfDeparture = _findNames[numberUserInput];
                            ShowTextOfMenu(9);
                            numberLineMenu = 4;
                            Console.ReadLine();
                            Console.Clear();
                        }
                    }

                    _findNames.Clear();
                }
            }

            if (isRouteSelect == true)
            {
                numberTicket = CreateTickets(cityOfDeparture, cityOfArrival);
                numberPassengers = SellTicket();

                ShowTextOfMenu(11);
                Console.WriteLine($"    {cityOfDeparture}");
                ShowTextOfMenu(12);
                Console.WriteLine($"    {cityOfArrival}");
                ShowTextOfMenu(13);
                Console.WriteLine($"    {numberPassengers}");
                ShowTextOfMenu(15);
                Console.ReadLine();

                _formedTrainsDeparture.Add(new FormedTrainDeparture(CreateTrain(numberPassengers), cityOfArrival, cityOfDeparture, numberPassengers));
            }

            Console.Clear();
        }

        private bool FindCity(string userInput)
        {
            bool isCityFind = false;

            foreach (var city in _cities)
            {
                if (city.ToLower().Contains(userInput.ToLower()))
                {
                    _findNames.Add($"{city}");
                    isCityFind = true;
                }
            }

            return isCityFind;
        }

        private void ShowSelectCities()
        {
            int i = 0;

            foreach (var selectName in _findNames)
            {
                Console.WriteLine($"    {i} - {selectName}");
                i++;
            }
        }

        private bool GetNumberCity(string userInput, out int numberUserInput)
        {
            numberUserInput = 0;
            bool isSelectTrue;

            if (int.TryParse(userInput, out int tryNumber) == false | tryNumber > _findNames.Count - 1 | tryNumber < 0)
            {
                isSelectTrue = false;
            }
            else
            {
                numberUserInput = tryNumber;
                isSelectTrue = true;
            }

            return isSelectTrue;
        }

        private void ShowTextOfMenu(int topic)
        {
            ConsoleColor color = ConsoleColor.Green;
            Console.ForegroundColor = color;

            Console.WriteLine($"    {_textOfMenu[topic]}");
            Console.ResetColor();
        }

        private int CreateTickets(string cityOfDeparture, string cityOfArrival)
        {
            Random random = new();
            int minNumberTicket = 81;
            int maxNumberTicket = 946;
            int numberTicket = random.Next(minNumberTicket, maxNumberTicket);

            for (int i = 0; i <= numberTicket; i++)
            {
                _tickets.Add(new Ticket(cityOfDeparture, cityOfArrival));
            }

            return numberTicket;
        }

        private int SellTicket()
        {
            Random random = new();
            int deviationNumberPossibleBuyers = 50;
            int numberPassengers = random.Next(_tickets.Count - deviationNumberPossibleBuyers, _tickets.Count + deviationNumberPossibleBuyers);

            for (int i = 0; i <= _tickets.Count - 1; i++)
            {
                _groupPassengers.Add(new Passenger(_tickets[i]));
            }

            return numberPassengers;
        }

        private void ShowInformationDepartureAllTrains()
        {
            Console.Clear();

            if (_formedTrainsDeparture.Count != 0)
            {
                foreach (FormedTrainDeparture trainDeparture in _formedTrainsDeparture)
                {
                    ShowTextOfMenu(20);

                    ConsoleColor color = ConsoleColor.Red;
                    Console.ForegroundColor = color;

                    Console.WriteLine($"    {_formedTrainsDeparture.IndexOf(trainDeparture)}");
                    ShowTextOfMenu(11);
                    Console.WriteLine($"    {trainDeparture.CityOfDeparture}");
                    ShowTextOfMenu(12);
                    Console.WriteLine($"    {trainDeparture.CityOfArrival}");
                    ShowTextOfMenu(13);
                    Console.WriteLine($"    {trainDeparture.NumberPassenger}");
                    ShowTextOfMenu(16);
                    Console.WriteLine($"    {trainDeparture.FormedTrain.NumberWagon}\n\n");
                }
            }
            else
            {
                ShowTextOfMenu(17);
            }

            Console.ReadLine();
            Console.Clear();
        }

        private void ShowInformationDepartureLastTrain()
        {
            Console.Clear();

            _cursorPositionX = 0;
            _cursorPositionY = 8;

            if (_formedTrainsDeparture.Count != 0)
            {
                Console.SetCursorPosition(_cursorPositionX, _cursorPositionY);
                ConsoleColor color = ConsoleColor.Red;
                Console.ForegroundColor = color;

                FormedTrainDeparture trainDeparture = _formedTrainsDeparture.Last();
                {
                    ShowTextOfMenu(11);
                    Console.WriteLine($"    {trainDeparture.CityOfDeparture}");
                    ShowTextOfMenu(12);
                    Console.WriteLine($"    {trainDeparture.CityOfArrival}");
                    ShowTextOfMenu(13);
                    Console.WriteLine($"    {trainDeparture.NumberPassenger}");
                    ShowTextOfMenu(16);
                    Console.WriteLine($"    {trainDeparture.FormedTrain.NumberWagon}");
                }

                Console.ResetColor();
            }

        }

