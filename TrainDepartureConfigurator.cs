    class Train
    {
        private List<Wagon> _composition = new();

        public int NumberWagon { get; private set; }
        public int NumberPassengers { get; private set; }

        public Train(int numberPassengers)
        {
            NumberPassengers = numberPassengers;
            GetComposition(numberPassengers);
            NumberWagon = _composition.Count;
        }

        private void GetComposition(int numberPassengers)
        {
            Random random = new();
            int number;
            int numberSeats;
            string[] wagonName = { "Вагон с общими местами", "Вагон плацкартный", "Вагон купейный" };

            Dictionary<string, int> Wagon = new()
            {
                { wagonName[0], 81 },
                { wagonName[1], 54 },
                { wagonName[2], 36 }
            };

            while (numberPassengers >= 0)
            {
                number = random.Next(1, Wagon.Count);
                _composition.Add(new Wagon(Wagon[wagonName[number]], wagonName[number]));
                numberSeats = Wagon[wagonName[number]];
                numberPassengers -= numberSeats;
            }
        }
    }

